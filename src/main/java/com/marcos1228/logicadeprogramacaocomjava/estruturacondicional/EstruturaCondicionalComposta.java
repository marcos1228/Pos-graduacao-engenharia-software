package com.marcos1228.logicadeprogramacaocomjava.estruturacondicional;

public class EstruturaCondicionalComposta {
//    // TODO DO
//    Resumo: Estrutura Condicional Composta em Java
//    A estrutura condicional composta permite definir dois caminhos de execução no programa:
//
//    Um bloco de código é executado se a condição for verdadeira (if).
//
//    Outro bloco de código é executado se a condição for falsa (else).
public static void main(String[] args) {

    int idade = 16;

    if (idade >= 18) {
        System.out.println("Você é maior de idade.");
    } else {
        System.out.println("Você é menor de idade.");
    }
}
}
