package com.marcos1228.logicadeprogramacaocomjava.estruturacondicional;

public class EstruturaCondicionalSimples {
    // Resumo: Estrutura Condicional Simples em Java
    //A estrutura condicional simples permite que um bloco de código seja executado somente se uma condição for verdadeira. Em Java, utilizamos a instrução if para implementar essa lógica.
    public static void main(String[] args) {
        int nota = 6;

        if(nota >= 6){
            System.out.println("APROVADO");
        }
    }
}
